                <p align="justify">Os 13 Programas de Educação Tutorial (PETs) da Universidade Federal do Maranhão (UFMA) têm por objetivo desenvolver atividades de ensino, pesquisa e extensão em diferentes locais do estado do Maranhão, dentro e fora da Universidade. A história dos grupos PET da UFMA começou em meados dos anos 1980, poucos anos após a implantação dos primeiros grupos PET no país, e tem se transformado conforme a uma legislação nacional que rege os grupos em todo o país. Os grupos PET são compostos por estudantes e professoras(es) de diferentes cursos de graduação da UFMA. O PET UFMA fortalece o ensino universitário ao realizar trabalhos de pesquisa e extensão em escolas públicas de São Luís, em comunidades populares quilombolas, assentamentos da reforma agrária e outros espaços rurais, além de desenvolver diferentes atividades no âmbito da Universidade. Uma dessas atividades se refere aos Encontros Nacionais, Regionais e Locais realizados pelos grupos PET. Em 2020, um desses eventos, o Encontro Nordestino dos Grupos PET (ENEPET), que está em sua décima nona edição, será realizado na UFMA, seguindo a proposta da alternância entre todos os estados nordestinos, e contará com a participação de aproximadamente 800 pessoas.
